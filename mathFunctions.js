// Math.PI - gets the PI value
console.log(Math.PI);

// CEIL Rounds up to the nearest integer
console.log(Math.ceil(Math.PI))

// MATH.floor
console.log(Math.floor(Math.PI))

// Math.pow returns the value of 4 raised to the power of 2
// Math.pow(x, y) .The console will return the value of x to the power of y
var power = Math.pow(4, 2);
console.log(power);

// Math.sqrt returns squareroot of the given number
console.log(Math.sqrt(16))

// Math.max returns maximum value in a list of argument
var maximum = Math.max(45, -76, 86, 7, 1, 0, 1024, 67, -346);
console.log(maximum);

// Math.random - Return a random number between 0 and 1, 
// 0 is part of the values that are returned while 1 isn't.
console.log(Math.random())